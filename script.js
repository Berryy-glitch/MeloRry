// =========================
// OPEN PLAYLIST
// =========================
function openPlaylist(
  sceneId, 
  songsArray, 
  containerId
) 

{
  // HIDE ALL SCENE
  document
    .querySelectorAll(".scene")
    .forEach(scene => {
      scene.classList.remove("active");
    });
  
  // SHOW PLAYLIST
  document
    .getElementById(sceneId)
    .classList.add("active");

  currentScene = "playlist";
  currentPlaylist = sceneId;

   // ACTIVE SONGS
  currentSongs = songsArray;

  // ACTIVE CONTAINER
  currentContainer =
    document.getElementById(containerId);

  // ACTIVE SLIDER
  currentSlider =
    document.querySelector(
      `#${sceneId} .slider`
    );

  // ACTIVE SLIDE
  currentSlide =
    document.querySelector(
      `#${sceneId} .slide`
    );
  
  // RENDER SONGS
  renderSongs(
    songsArray, 
    containerId);

  setupSlider();
}


// =========================
// RENDER SONGS
// =========================
function renderSongs(songsArray, containerId) 
{
  const container =
    document.getElementById(containerId);

  container.innerHTML = "";

  songsArray.forEach((song, index) => {

    const songElement =
      document.createElement("div");

      songElement.dataset.index =
       index;

    songElement.classList.add("song");

    songElement.innerHTML = `

      <div class="thumbnail">

        <img src="${song.image}">

      </div>

      <div class="info">

        <p class="tittle">
          ${
              currentPlayingSong === song
                ? "♪ " + (song.shortTitle || song.title)
                : (song.shortTitle || song.title)
            }
        </p>

        <p class="singer">
          ${song.shortArtist || song.artist}
        </p>

      </div>

    `;

    songElement.addEventListener("click", () => {

      if (
        currentSongIndex === index &&
        !audioPlayer.paused
      ) {

        document
          .querySelectorAll(".scene")
          .forEach(scene => {
            scene.classList.remove("active");
          });

        document
          .getElementById("playerScene")
          .classList.add("active");

        currentScene = "player";

        return;
      }

      playSong(index);

    });

    container.appendChild(songElement);
  });
}

