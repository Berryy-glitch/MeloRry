function goHome() {

  // dari PLAYER → PLAYLIST
  if (
    document
      .getElementById("playerScene")
      .classList.contains("active")
  ) {

    document
      .querySelectorAll(".scene")
      .forEach(scene => {
        scene.classList.remove("active");
      });

    document
      .getElementById(currentPlaylist)
      .classList.add("active");

    // render ulang supaya ♪ muncul
    if (currentPlaylist === "playlistSceneserotonin") {

      renderSongs(
        serotoninBoostSongs,
        "serotoninContainer"
      );
    }

    else if (currentPlaylist === "playlistScenefuel") {

      renderSongs(
        brainFuelSongs,
        "brainContainer"
      );
    }

    else if (currentPlaylist === "playlistSceneanalog") {

      renderSongs(
        analogLoveSongs,
        "analogContainer"
      );
    }

    setTimeout(() => {

      scrollToCurrentSong();

    }, 50);

    return;
  }

  // dari PLAYLIST → HOME
  const playlistScene =
    document.querySelector(
      "#playlistSceneserotonin.active, #playlistScenefuel.active, #playlistSceneanalog.active"
    );

  if (playlistScene) {

    document
      .querySelectorAll(".scene")
      .forEach(scene => {
        scene.classList.remove("active");
      });

    document
      .getElementById("homeScene")
      .classList.add("active");
  }
}


