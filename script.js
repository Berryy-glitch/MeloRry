// =========================
// SLIDER SCROLL LETTER
// =========================
let noteDragging = false;

function setupNoteSlider(playlistPage) {

      const noteContent =
        playlistPage.querySelector(".note-content");

      const noteSlider =
        playlistPage.querySelector(".note-slider");

      const noteSlide =
        playlistPage.querySelector(".note-slide");

      // BLOCK MOUSE SCROLL
      noteContent.addEventListener(
        "wheel",
        (e) => {
          e.preventDefault();
        },
        { passive: false }
      );

      // UPDATE SLIDER
      noteContent.onscroll = () => {

      const scrollTop =
        noteContent.scrollTop;

      const scrollHeight =
        noteContent.scrollHeight
        - noteContent.clientHeight;

      const scrollPercent =
        scrollTop / scrollHeight;

      const maxSliderMove =
        noteSlide.clientHeight
        - noteSlider.clientHeight;

      noteSlider.style.top =
        scrollPercent * maxSliderMove + "px";
    };

    // =========================
    // DRAG SYSTEM
    // =========================

    // START DRAG
    noteSlider.onmousedown = () => {
      noteDragging = true;
    };
   
    // STOP DRAG
    document.addEventListener(
      "mouseup",
      () => {
        noteDragging = false;
      }
    );
    
    // DRAGGING
    document.addEventListener(
    "mousemove",
    (e) => {

      if (!noteDragging) return;

      const slideRect =
        noteSlide.getBoundingClientRect();

      let offsetY =
        e.clientY - slideRect.top;

      const maxMove =
        noteSlide.clientHeight
        - noteSlider.clientHeight;

      if (offsetY < 0) {
        offsetY = 0;
      }

      if (offsetY > maxMove) {
        offsetY = maxMove;
      }

      noteSlider.style.top =
        offsetY + "px";

      const scrollPercent =
        offsetY / maxMove;

      const maxScroll =
        noteContent.scrollHeight
        - noteContent.clientHeight;

      noteContent.scrollTop =
        scrollPercent * maxScroll;
    }
  );
}