    document
      .getElementById("playerAlbum")
      .style.color =
        song.theme?.titleBg || "#ffff";
    document
      .getElementById("playerAlbum")
      .innerText = song.album;
    
    document
      .getElementById("playerCover")
      .src = song.image;


  // =========================
  // CHANGE PLAYER COLORS
  // =========================
  document
    .querySelector(".player-page")
    .style.background =
      song.colors?.page || "#D9D9D9";

  document
    .querySelector(".now-playing")
    .style.background =
      song.colors?.header || "#B3B3B3";
  document
    .getElementById("playerNow")
    .style.color =
      song.theme?.titleBg || "#ffff";

  document
    .querySelector(".music-card")
    .style.background =
      song.colors?.card || "#B3B3B3";

  document
    .querySelector("#progress")
    .style.setProperty(
      "--progress-color",
      song.colors?.progress || "#B3B3B3"
    );


  // =========================
  // CHANGE SCENE
  // =========================
    document
      .querySelectorAll(".scene")
      .forEach(scene => {
        scene.classList.remove("active");
      });

    document
      .getElementById("playerScene")
      .classList.add("active");

    currentScene = "player";

